        <p  >Plasma nitriding (also known as ionitriding, pulse plasma nitriding, as well as cold nitriding or plasma hardening) is a thermochemical heat treatment process that is used to increase the reliability and wear resistance of mechanically stressed metal components. Fatigue strength as well as corrosion protection of materials are improved by surface treatment in a particularly gentle way. Under the influence of heat, plasma nitriding causes a chemical transformation of the surface layer by diffusion of nitrogen, which forms nitrides together with the material of the treated material. This leads to increased surface hardness and significantly improved resistance to wear.
 
    
 </p>
         <p >

         Compared to conventional hardening processes, the workpieces are treated at significantly lower temperatures, which ensures high dimensional accuracy in this type of heat treatment. Since, as a consequence, a costly reworking of the workpieces in the surface 
         </p>




      
    

         <p >hardened condition is no longer necessary or can be reduced to a minimum, plasma nitriding allows additional cost savings to be realized within the process chain.</p>
 
 

         <p >

The material to be treated can often be manufactured to final dimensions in the soft state and can be finished after heat treatment in the plasma with little or no reworking. Furthermore, very low tempered, heat- treated steels can be treated without loss of core strength.
</p>

<p >

In principle, nitriding is possible with various processes. Well known besides plasma nitriding are bath nitriding and gas nitriding. Among the hardening processes, plasma nitriding has a special position due to its reproducibility, environmental compatibility and energy efficiency.
</p>

<h3 >The nitriding layer and its function</h3>

<p >The nitriding layer consists of the outer bonding layer (VS) and the diffusion layer (DS) underneath. The compound layer is composed of iron nitrides – the nitrogen-rich ε nitride Fe2-3N and the iron-rich γ`-Nitride Fe4N. In comparison to gas nitriding, the compound layer produced by plasma nitriding is more compact, has fewer pores and therefore has better layer properties.</p>
<p >Below the VS is the diffusion zone (DS), which is composed of the base material with precipitated nitrides. The more nitride-forming elements are present in the steel, the greater the attainable surface hardness. This explains why unalloyed steels only achieve surface hardnesses of 250-300 HV, low-alloy steels 600-700 HV and nitriding and high-alloy steels 800-1200 HV.</p>

<p >The characteristic value nitriding depth NHT is defined as the edge distance at which core hardness +50 HV is present (according to DIN50190 Part 3). Usual NHT are:</p>

<ul>
<li >up to 0.8 mm for unalloyed and low-alloy steels</li>
<li >up to 0.15 mm for high-alloy steels and stainless steel</li>
</ul>
<p >The depth that can be reached and the time in which it can be reached is largely determined by the material used, the treatment temperature and the treatment time.</p>

<p >If particularly thick compound layers are required, plasma nitrocarburizing is recommended as an alternative to plasma nitriding. To increase the corrosion resistance of low and medium alloyed materials, there is also the possibility of post-oxidation. With the help of this additional step, the corrosion protection can be further increased after plasma nitriding.</p>

 
 
