                        <p class="mb-4" style="text-align: justify;">Plasma nitriding (Ion nitriding) is a plasma supported thermochemical case hardening process used to increase wear resistance, surface hardness and fatigue by generation of a hard layer including compressive stresses.
                            The advantages of gaseous nitriding processes can be surpassed by plasma nitriding. Particularly when applied to higher alloyed steels, plasma nitriding imparts a high surface hardness which promotes high resistance to wear, scuffing, galling and seizure. Fatigue strength is increased mainly by the development of surface compressive stresses. Plasma nitriding is a smart choice whenever parts are required to have both nitrided and soft areas. The possibility of generating a compound layer free diffusion layer is often used in plasma nitriding prior to PVD or CVD coating. Tailor made layers and hardness profiles can be achieved.
    
                        </p>
                                <p style="text-align: justify;">
                                    Plasma nitriding/ion nitriding is a modern thermochemical treatment which is carried out in a mixture of nitrogen, hydrogen and an optional carbon spending gas. In this low pressure process, a voltage is applied between the batch and the furnace wall. A glow discharge with a high ionisation level (plasma) is generated around the parts. On the surface area that is directly charged by the ions, nitrogen-rich nitrides are formed and decompose, releasing active nitrogen into the surface. Due to this mechanism shielding is easily done by covering the concerning areas with a metal blanket.
                                </p>
                            
                            <p style="text-align: justify;">
    
                                Plasma nitriding allows modification of the surface according to the desired properties. Tailor made layers and hardness profiles can be achieved by adapting the gas mixture: from a compound layer-free surface with low nitrogen contents up to 20 microns thick, to a compound layer with high nitrogen contents and an add-on of carbonic gas (plasma nitro-carburation). The wide applicable temperature range enables a multitude of applications, beyond the possibilities of gas or salt bath processes.
