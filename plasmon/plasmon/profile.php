                        <h5 class="fw-bold text-warning text-uppercase">About Ion Nitriding</h5>
                        <h1 class="mb-0">Ion Nitriding</h1>
                    </div>
                    <p class="mb-3" style="text-align: justify;">Plasma Ion Nitriding is a process for surface treatment of metal parts to make them resistant to wear and fatigue. A more modern development of the nitriding process, also known as ion nitriding. In this process the component is made cathodic in relation to the furnace shell and nitrogen or ammonia gas is fed into the evacuated chamber.
                    </p>
                            <p style="text-align: justify;">

                                The glow discharge at the surface of the steel part produces atomic nitrogen by ionisation of the nitrogen or ammonia gas. Although this process uses more expensive equipment, it has the advantage of being extremely controllable. Also it has a time advantage compared with conventional gas nitriding and lower nitriding temperatures can be used (450/590 C).
                            </p>
                       
                        <p style="text-align: justify;">

                           Nitriding commences as soon as the surface ionisation occurs, and since it is not necessary to wait until the whole cross section of the component reaches nitriding temperature, cycle times are shorter.
